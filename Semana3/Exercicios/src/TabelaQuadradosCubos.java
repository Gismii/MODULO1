public class TabelaQuadradosCubos {
    public static void main(String[] args){
       // Scanner scanner =new Scanner(System.in);
        System.out.println("NUMERO  SQUARE  CUBO ");


        int n=0;
        for (int i =0; i<=10; i++){


          System.out.println(n+    "         "   +n*n+"       "+(n*n*n));

            n++;
        }
    }
}

